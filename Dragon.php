Instead, that encounter turned into an unspoken pact. Over the next few months, Ellis returned often, bringing food and listening to the dragon’s stories of the world before hers. In turn, she shared village tales of ordinary lives, of seasons and harvests. A strange, mutual respect grew, and though Ellis never told anyone about her visits, the village began to thrive mysteriously—no missing livestock, better harvests, fewer storms. <br> <br>


As she reached the cloud-shrouded heights, a deep rumble stopped her in her tracks. She turned a corner and found herself face to face with the dragon. It was immense, its scales a muted green, blending into the mountainside like moss on stone. Its eyes held her in a steady, calculating gaze. <br> <br>

“Why are you here?” it asked, its voice a deep murmur that seemed to come from the mountain itself. <br> <br>

